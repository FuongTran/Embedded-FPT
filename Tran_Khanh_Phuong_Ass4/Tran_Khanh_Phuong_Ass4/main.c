#define _CRT_SECURE_NO_DEPRECATE

/*******************************************************************************
* Include
*******************************************************************************/
#include <stdio.h>
#include <stdlib.h>
#include "allocate.h"

/*******************************************************************************
* Main
*******************************************************************************/
int main() {
    uint8_t* ptr = NULL;

    if (allocate10Bytes((uint8_t*)&ptr)) {
        printf("Memory allocated successfully.\n");

        ptr[1] = 2;
        ptr[0] = 3;

        free(ptr);
    }
    else {
        printf("Memory allocation failed.\n");
    }

    return 0;
}
